const fs = require("fs");
const path = require("path");

const root = process.cwd();

function normalize(p) {
  return String(p || "").replace(/\\/g, "/").replace(/^\/+/, "");
}

function collectHtml(baseDir, rel = "") {
  const dir = path.join(baseDir, rel);
  const out = [];
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    if (ent.name.startsWith(".") || ent.name === "node_modules") continue;
    const child = normalize(path.join(rel, ent.name));
    if (ent.isDirectory()) out.push(...collectHtml(baseDir, child));
    else if (ent.isFile() && ent.name.endsWith(".html")) out.push(child);
  }
  return out;
}

const pages = collectHtml(root).sort((a, b) => a.localeCompare(b));
const rootPages = pages.filter((p) => !p.includes("/"));
const esPages = pages.filter((p) => p.startsWith("es/"));
const usPages = pages.filter((p) => p.startsWith("us/"));
const otherNested = pages.filter((p) => p.includes("/") && !p.startsWith("es/") && !p.startsWith("us/"));

const lines = [];
lines.push("# Pages Inventory", "");
lines.push(`Total HTML pages: ${pages.length}`, "");

lines.push(`## Root Pages (${rootPages.length})`);
for (const p of rootPages) lines.push(`- ${p}`);
lines.push("");

lines.push(`## Spanish Pages /es (${esPages.length})`);
for (const p of esPages) lines.push(`- ${p}`);
lines.push("");

lines.push(`## U.S. Pages /us (${usPages.length})`);
for (const p of usPages) lines.push(`- ${p}`);
lines.push("");

if (otherNested.length) {
  lines.push(`## Other Nested Pages (${otherNested.length})`);
  for (const p of otherNested) lines.push(`- ${p}`);
  lines.push("");
}

lines.push("Generated by `node scripts/refresh-pages-inventory.js`.");
fs.writeFileSync(path.join(root, "pages-inventory.md"), lines.join("\n"), "utf8");
console.log(`Wrote pages-inventory.md (${pages.length} HTML pages).`);
